// 1. Number
let age=19;
console.log(age);
console.log(typeof(age));
console.log("\n");

// Number
let price=250.00;
console.log(price);
console.log(typeof(price));
console.log("\n");

// 2. String
let fullName="Tony Stark";
console.log(fullName);
console.log(typeof(fullName));
console.log("\n");

// 3. Boolean
let isFollow=true;
console.log(isFollow);
console.log(typeof(isFollow));
console.log("\n");

// 4. Undefined
let x;
console.log(x);
console.log(typeof(x));
console.log("\n");

// 5. Null
let y=null;
console.log(y);
console.log(typeof(y));
console.log("\n");

// 6. BigInt
let p=BigInt("1955");
console.log(p);
console.log(typeof(p));
console.log("\n");

// 7. Symbol
let q=Symbol("Hello JII");
console.log(q);
console.log(typeof(q));
console.log("\n");